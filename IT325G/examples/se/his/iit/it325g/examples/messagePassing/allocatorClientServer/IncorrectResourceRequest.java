package se.his.iit.it325g.examples.messagePassing.allocatorClientServer;

public class IncorrectResourceRequest extends Exception {

	public IncorrectResourceRequest() {
		super();
	}

	public IncorrectResourceRequest(String arg0) {
		super(arg0);
	}

	public IncorrectResourceRequest(Throwable arg0) {
		super(arg0);
	}

	public IncorrectResourceRequest(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IncorrectResourceRequest(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
